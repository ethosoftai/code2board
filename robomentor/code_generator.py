"""Firmware (sketch) generator.

Generates Arduino-C++ firmware whose pin ``#define``s come **directly** from the
circuit's ``pin_map``, guaranteeing the code-circuit validator can match them.
Every sketch includes:

* named pin constants with wiring comments,
* readable helper functions,
* serial debug logging,
* a tunable motor/output speed where relevant,
* a ``SELF_TEST`` mode toggle,
* beginner-friendly comments.

Boards that run the Arduino core (Uno/Nano/Mega/ESP32/Pico/STM32duino) all use
the Arduino-C++ generator.  An optional MicroPython variant is produced for
ESP32/Pico when requested.
"""

from __future__ import annotations

from typing import Dict, List

from .schemas import Circuit, Firmware, PlannerResult


HEADER = """// ---------------------------------------------------------------------------
// {title}
// Generated by RoboMentor — material-aware robotics education toolchain.
// Board: {board}
//
// This sketch was generated to match the wiring diagram exactly. Every pin
// constant below corresponds to a connection in the circuit diagram.
// Read the comments — they explain WHAT each pin does and WHY.
// ---------------------------------------------------------------------------

// Set SELF_TEST to 1 to cycle outputs once at startup and print a checklist,
// which is handy for confirming your wiring before running the real program.
#define SELF_TEST 0
"""


def _defines(pin_map: Dict[str, str], comments: Dict[str, str]) -> str:
    lines: List[str] = []
    for name, pin in pin_map.items():
        comment = comments.get(name, "")
        suffix = f"  // {comment}" if comment else ""
        # Arduino analog pins (A0..) are valid tokens; numeric pins as ints.
        value = pin if pin.startswith("A") or pin.startswith("GP") or pin.startswith("D") else pin
        lines.append(f"#define {name} {value}{suffix}")
    return "\n".join(lines)


# --------------------------------------------------------------------------- #
# Motor driver code fragments (shared by line follower + obstacle avoider)
# --------------------------------------------------------------------------- #
def _is_tb6612(pin_map: Dict[str, str]) -> bool:
    return "PWMA" in pin_map


def _motor_setup(pin_map: Dict[str, str]) -> str:
    if _is_tb6612(pin_map):
        pins = ["AIN1", "AIN2", "BIN1", "BIN2", "PWMA", "PWMB", "STBY"]
    else:
        pins = ["IN1", "IN2", "IN3", "IN4", "ENA", "ENB"]
    out = "\n".join(f"  pinMode({p}, OUTPUT);" for p in pins if p in pin_map)
    if _is_tb6612(pin_map):
        out += "\n  digitalWrite(STBY, HIGH); // enable the TB6612 driver"
    return out


def _motor_functions(pin_map: Dict[str, str]) -> str:
    if _is_tb6612(pin_map):
        return """
// --- Motor control (TB6612FNG) ------------------------------------------
void leftMotor(int speed, bool forward) {
  digitalWrite(AIN1, forward ? HIGH : LOW);
  digitalWrite(AIN2, forward ? LOW : HIGH);
  analogWrite(PWMA, speed);
}
void rightMotor(int speed, bool forward) {
  digitalWrite(BIN1, forward ? HIGH : LOW);
  digitalWrite(BIN2, forward ? LOW : HIGH);
  analogWrite(PWMB, speed);
}
void stopMotors() { analogWrite(PWMA, 0); analogWrite(PWMB, 0); }
"""
    return """
// --- Motor control (L298N) ----------------------------------------------
void leftMotor(int speed, bool forward) {
  digitalWrite(IN1, forward ? HIGH : LOW);
  digitalWrite(IN2, forward ? LOW : HIGH);
  analogWrite(ENA, speed);   // ENA must be a PWM pin for speed control
}
void rightMotor(int speed, bool forward) {
  digitalWrite(IN3, forward ? HIGH : LOW);
  digitalWrite(IN4, forward ? LOW : HIGH);
  analogWrite(ENB, speed);
}
void stopMotors() { analogWrite(ENA, 0); analogWrite(ENB, 0); }
"""


